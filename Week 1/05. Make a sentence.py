# Create a proper sentence by using f-strings to combine
# the variables below into a nice sentence.
# Add your own strings, if necessary, to make it readable.

q1 = "By seeking"          # first part of the sentence
q2 = "and"                 # connector word
q3 = "blundering"          # action word (means making mistakes)
q4 = "we learn."           # ending of the sentence

# Combine all the parts into a proper sentence using an f-string
final_sentence = f"{q1} {q2} {q3}, {q4}"   # f-string inserts the variables into one formatted sentence

# Print the final sentence
print(final_sentence)       # prints the completed sentence
